# PowerShell script to initialize environment variables and database for CITE-ES
# Run this from the project root (college_of_information_technology2)
# Usage: .\scripts\setup-env.ps1

param(
    [string]$DbHost = "localhost",
    [string]$DbPort = "3306",
    [string]$DbUser = "root",
    [string]$DbPassword = "",
    [string]$DbName = "cite_es",
    [string]$JwtSecret = $(New-Guid).Guid,
    [string]$ApiUrl = "http://localhost:3000/api",
    [switch]$Force
)

# helper to write a key=value pair into .env.local
function Set-EnvVar {
    param($Path, $Key, $Value)
    $escaped = $Value -replace '\\', '\\\\'
    if (-not (Test-Path $Path) -or $Force) {
        "Creating $Path" | Write-Host
        "# generated by setup-env.ps1`n" | Out-File -FilePath $Path -Encoding utf8
    }
    if (Select-String -Path $Path -Pattern "^$Key=" -Quiet) {
        (Get-Content $Path) |
            ForEach-Object { $_ -replace "^$Key=.*", "$Key=$escaped" } |
            Set-Content $Path -Encoding utf8
    } else {
        Add-Content $Path "$Key=$escaped"
    }
}

$envFile = ".env.local"

Write-Host "[1/3] Writing environment variables to $envFile"
Set-EnvVar $envFile "DB_HOST" $DbHost
Set-EnvVar $envFile "DB_PORT" $DbPort
Set-EnvVar $envFile "DB_USER" $DbUser
Set-EnvVar $envFile "DB_PASSWORD" $DbPassword
Set-EnvVar $envFile "DB_NAME" $DbName
Set-EnvVar $envFile "JWT_SECRET" $JwtSecret
Set-EnvVar $envFile "NEXT_PUBLIC_API_URL" $ApiUrl
Set-EnvVar $envFile "NODE_ENV" "development"

Write-Host "[2/3] Initializing database schema (requires mysql in PATH)"
if (Get-Command mysql -ErrorAction SilentlyContinue) {
    # pipe the schema file into mysql since PowerShell does not support '<' redirection
    try {
        Get-Content "database/schema.sql" | & mysql -h $DbHost -P $DbPort -u $DbUser -p$DbPassword $DbName
        if ($LASTEXITCODE -ne 0) {
            Write-Warning "Database import returned non-zero exit code ($LASTEXITCODE). Check credentials or run manually."
        } else {
            Write-Host "Database schema applied to $DbName"
        }
    } catch {
        Write-Warning "Failed to execute mysql command: $_"
    }
} else {
    Write-Warning "mysql command not found; please import database manually."
}

Write-Host "[3/3] Environment setup complete."
Write-Host "Run 'npm install' and then 'npm run dev' to start the server."
